# Import the 'sys' module, which provides access to some variables used or maintained by the interpreter.
import sys
# Print the string "Python version" to indicate that we are about to display the Python version.
print("python.version")
# Using the 'sys.version' attribute to obtain and print the Python version information.
print(sys.version)
# Print the string "Version info" to indicate that we are about to display version information.
print("verison_info")
# Use the 'sys.version_info' attribute to obtain and print detailed version information about Python.
print(sys.version_info)
