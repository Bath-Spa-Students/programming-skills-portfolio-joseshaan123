# Create a list called 'names' containing four elements.
names = ['Shaan', 'jose', 'Baiju', 'Joseph']
# Create a message by combining "hello," with the first name from the 'names' list (capitalized using the title() method).
msg = "hello," + names[0].title() + "!"
# Print the message, which will greet the first person in the list with a capitalized name.
print(msg)
# Repeat the process for the second person in the list, creating a message and printing it.
msg = "hello," + names[1].title() + "!"
print(msg)
# Repeat the process for the third person in the list, creating a message and printing it.
msg = "hello," + names[2].title() + "!"
print(msg)
